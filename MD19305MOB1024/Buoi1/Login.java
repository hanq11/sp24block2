    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String userName = txtUsername.getText();
        String password = txtPassword.getText();
        if(userName.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui long nhap username");
            txtUsername.requestFocus();
        } else if(password.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui long nhap password");
            txtPassword.requestFocus();
        }
        
        if(userName.equals("hanq11") && password.equals("123")) {
            JOptionPane.showMessageDialog(this, "Dang nhap thanh cong");
        } else {
            JOptionPane.showMessageDialog(this, "Dang nhap that bai");
        }
    }                                        

