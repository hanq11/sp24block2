    public ManHinh() {
        initComponents();
        ArrayList<NhanVien> danhSach = nhanVienRepository.search();
        loadTable(danhSach);
        
        int tblSize = danhSach.size();
        NhanVien lastNhanVien = danhSach.get(tblSize - 1);
        txtMa.setText(lastNhanVien.getMa());
        txtTen.setText(lastNhanVien.getTen());
        txtSdt.setText(lastNhanVien.getSdt());
        if(lastNhanVien.getTrangThai() == 1) {
            rdoDangLam.setSelected(true);
        } else {
            rdoNghiViec.setSelected(true);
        }
    }
    
    public void loadTable(ArrayList<NhanVien> danhSach) {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        for(NhanVien nv: danhSach) {
            Object[] thongTin = {nv.getMa(), nv.getTen(), nv.getSdt(), nv.getTrangThai() == 1 ? "Dang lam" : "Nghi viec"};
            model.addRow(thongTin);
        }
    }
