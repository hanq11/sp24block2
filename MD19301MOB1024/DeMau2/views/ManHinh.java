    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int selected = tblNhanVien.getSelectedRow();
        txtMa.setText(tblNhanVien.getValueAt(selected, 0).toString());
        txtTen.setText(tblNhanVien.getValueAt(selected, 1).toString());
        txtSdt.setText(tblNhanVien.getValueAt(selected, 2).toString());
        if(tblNhanVien.getValueAt(selected, 3).toString().equals("Dang lam")) {
            rdoDangLam.setSelected(true);
        } else {
            rdoNghiViec.setSelected(true);
        }

    }//GEN-LAST:event_tblNhanVienMouseClicked

